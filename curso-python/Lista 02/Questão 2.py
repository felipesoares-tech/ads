'''2 - Faça um programa que receba um dia da semana de entrada, como número, e
escreva esse dia por extenso.'''

dia = int(input('Informe um dia na semana [1-7]: '))

if dia == 1:
    print('Domingo')
elif dia == 2:
    print('Segunda')
elif dia == 3:
    print('Terca')
elif dia == 4:
    print('Quarta')
elif dia == 5:
    print('Quinta')
elif dia == 6:
    print('Sexta')
elif dia == 7:
    print('Sabado')
